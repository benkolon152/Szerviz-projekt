function hashText(text) {
  let hash = 0;

  for (let index = 0; index < text.length; index += 1) {
    hash = (hash * 31 + text.charCodeAt(index)) >>> 0;
  }

  return hash;
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function parseNumber(name, regex, fallback) {
  const match = name.match(regex);

  return match ? Number(match[1]) : fallback;
}

function parseCapacityGb(name, fallback = 1000) {
  const tb = name.match(/(\d+)\s*TB/i);
  if (tb) return Number(tb[1]) * 1000;

  const gb = name.match(/(\d+)\s*GB/i);
  if (gb) return Number(gb[1]);

  return fallback;
}

function inferPriceHuf(category, name) {
  const noise = hashText(name) % 7000;

  if (category === "cpus") {
    let base = 65000;
    if (/Ryzen\s*9|Core\s*i9|Ultra\s*9/i.test(name)) base = 230000;
    else if (/Ryzen\s*7|Core\s*i7|Ultra\s*7/i.test(name)) base = 145000;
    else if (/Ryzen\s*5|Core\s*i5|Ultra\s*5/i.test(name)) base = 90000;
    else if (/Xeon/i.test(name)) base = 290000;
    if (/X3D/i.test(name)) base += 45000;
    return base + noise;
  }

  if (category === "motherboards") {
    let base = 60000;
    if (/X870|X670E|Z890|Z790|Taichi|Maximus/i.test(name)) base = 150000;
    else if (/B850|B760|B650|X570/i.test(name)) base = 90000;
    else if (/A620/i.test(name)) base = 50000;
    if (/WiFi/i.test(name)) base += 12000;
    return base + noise;
  }

  if (category === "pcCases") {
    let base = 30000;
    if (/O11|Y60|7000D|GT502|Vision/i.test(name)) base = 85000;
    else if (/Meshify|North|H7|5000D|Silent Base/i.test(name)) base = 60000;
    return base + noise;
  }

  if (category === "gpus") {
    let base = 120000;
    if (/4090|W7800|4080|7900\s*XTX/i.test(name)) base = 650000;
    else if (/4070|7900\s*XT|7800\s*XT/i.test(name)) base = 320000;
    else if (/4060|7700|7600|6750|6700|A770/i.test(name)) base = 190000;
    else if (/3050|1660|6500|A580|A750/i.test(name)) base = 110000;
    return base + noise * 2;
  }

  if (category === "rams") {
    const capacity = parseCapacityGb(name, 32);
    const isDdr5 = /DDR5/i.test(name);
    const base = isDdr5 ? 1200 * capacity : 900 * capacity;
    return base + 15000 + noise;
  }

  if (category === "psus") {
    const watt = parseNumber(name, /(\d{3,4})\s*W/i, 750);
    let base = watt * 85;
    if (/Titanium|Platinum|PX|TX|Dark Power|Leadex|Vertex/i.test(name)) base += 30000;
    if (/SFX/i.test(name)) base += 12000;
    return base + 12000 + noise;
  }

  if (category === "hdds") {
    const capacity = parseCapacityGb(name, 4000);
    return 9000 + capacity * 18 + noise;
  }

  if (category === "ssds") {
    const capacity = parseCapacityGb(name, 1000);
    let perGb = 35;
    if (/990\s*PRO|SN850X|KC3000|FireCuda|MP700|P44\s*Pro|CS3140/i.test(name)) perGb = 55;
    if (/870\s*EVO|MX500|BX500|A400|CS900/i.test(name)) perGb = 25;
    return 12000 + capacity * perGb + noise;
  }

  if (category === "opticalDrives") {
    let base = /Blu-ray|BDR|WH16/i.test(name) ? 45000 : 12000;
    if (/External|USB|ZenDrive|Slim/i.test(name)) base += 5000;
    return base + noise;
  }

  if (category === "cpuAirCoolers") {
    let base = 12000;
    if (/NH-D15|Dark Rock Pro|Assassin IV|AK620|Frost Commander/i.test(name)) base = 42000;
    else if (/NH-U12|AK500|Peerless|Phantom|Fuma|Mugen/i.test(name)) base = 26000;
    return base + noise;
  }

  if (category === "cpuLiquidCoolers") {
    let base = 35000;
    if (/360|420|Elite|Ryujin|HydroShift|K360/i.test(name)) base = 70000;
    if (/240/i.test(name)) base = Math.max(base - 12000, 28000);
    return base + noise;
  }

  if (category === "monitors") {
    let base = 85000;
    if (/OLED|UQX|AW3423|4K|U3223|PA329/i.test(name)) base = 260000;
    else if (/27|32|34/i.test(name)) base = 120000;
    if (/240|360|QD-OLED/i.test(name)) base += 90000;
    return base + noise * 2;
  }

  if (category === "keyboards") {
    let base = 18000;
    if (/Azoth|Q6|Q60|Q1|G915|BlackWidow\s*V4|Huntsman|K100/i.test(name)) base = 65000;
    else if (/K70|Apex\s*Pro|MX\s*Mechanical|Alloy\s*Rise/i.test(name)) base = 42000;
    return base + noise;
  }

  if (category === "mice") {
    let base = 12000;
    if (/Superlight|V3\s*Pro|V2\s*Pro|Naga\s*V2|Finalmouse|Harpe\s*Ace/i.test(name)) base = 52000;
    else if (/MX\s*Master|G502\s*X|Basilisk\s*V3\s*Pro|Aerox\s*5/i.test(name)) base = 34000;
    return base + noise;
  }

  if (category === "operatingSystems") {
    if (/Windows\s*Server/i.test(name)) return 320000 + noise;
    if (/Windows\s*11\s*Pro|Windows\s*10\s*Pro|Enterprise|Education|Workstations|IoT/i.test(name)) return 85000 + noise;
    if (/Windows\s*11\s*Home|Windows\s*10\s*Home|Windows\s*8\.1|Windows\s*7/i.test(name)) return 52000 + noise;
    if (/Red Hat Enterprise Linux/i.test(name)) return 210000 + noise;
    if (/TrueNAS|Proxmox/i.test(name)) return 45000 + noise;
    return 0;
  }

  return 10000 + noise;
}

function inferSpecifications(category, name) {
  const common = {
    brand: name.split(" ")[0],
    model: name,
  };

  if (category === "cpus") {
    const tier = /i9|Ryzen\s*9|Ultra\s*9/i.test(name)
      ? 9
      : /i7|Ryzen\s*7|Ultra\s*7/i.test(name)
        ? 7
        : /i5|Ryzen\s*5|Ultra\s*5/i.test(name)
          ? 5
          : 3;
    const cores = clamp(tier * 2, 4, 16);
    const boost = clamp(3.8 + tier * 0.25 + (hashText(name) % 6) * 0.1, 4.0, 5.9);
    return {
      ...common,
      socket: /Ryzen/i.test(name) ? "AM4/AM5" : "LGA1700/1851",
      cores,
      threads: cores * 2,
      boostClockGHz: Number(boost.toFixed(1)),
      integratedGraphics: !/F\b/i.test(name),
      tdpW: clamp(45 + tier * 20 + (hashText(name) % 15), 65, 170),
    };
  }

  if (category === "motherboards") {
    return {
      ...common,
      socket: /B5|X5|A620|AM4|AM5|X670|X870|B650|B550/i.test(name) ? "AM4/AM5" : "LGA1700/1851",
      formFactor: /-I\b|ITX/i.test(name) ? "Mini-ITX" : /M\b|Micro/i.test(name) ? "Micro-ATX" : "ATX",
      memoryType: /DDR4/i.test(name) ? "DDR4" : "DDR5",
      wifi: /WiFi/i.test(name),
      pcie: /X870|Z890|Z790|X670E/i.test(name) ? "PCIe 5.0" : "PCIe 4.0",
      m2Slots: clamp(2 + (hashText(name) % 4), 2, 5),
    };
  }

  if (category === "pcCases") {
    return {
      ...common,
      caseType: /Mini|D31|AIR 100|H5/i.test(name) ? "Mid Tower Compact" : "Mid Tower",
      motherboardSupport: ["ATX", "Micro-ATX", "Mini-ITX"],
      sidePanel: /TG|Glass|Vision|Y60|O11/i.test(name) ? "Tempered Glass" : "Steel",
      maxGpuLengthMm: 330 + (hashText(name) % 101),
      preinstalledFans: 2 + (hashText(name) % 4),
    };
  }

  if (category === "gpus") {
    const vram = parseNumber(name, /(\d+)\s*GB/i, /4090|7900\s*XTX|W7800/i.test(name) ? 24 : 12);
    return {
      ...common,
      memoryGB: vram,
      memoryType: /RTX|Arc/i.test(name) ? "GDDR6/GDDR6X" : "GDDR6",
      outputs: ["HDMI 2.1", "DisplayPort 1.4/2.1"],
      recommendedPsuW: clamp(450 + vram * 20, 550, 1000),
      rayTracing: /RTX|RX\s*7|Arc|W7800/i.test(name),
    };
  }

  if (category === "rams") {
    const capacity = parseCapacityGb(name, 32);
    const speed = parseNumber(name, /DDR\d-(\d{4,5})/i, /DDR5/i.test(name) ? 6000 : 3200);
    return {
      ...common,
      capacityGB: capacity,
      type: /DDR5/i.test(name) ? "DDR5" : "DDR4",
      speedMTs: speed,
      kit: capacity >= 64 ? "2x32GB" : capacity >= 32 ? "2x16GB" : "2x8GB",
      rgb: /RGB|Spectrix|Delta|Trident|Royal/i.test(name),
    };
  }

  if (category === "psus") {
    const watt = parseNumber(name, /(\d{3,4})\s*W/i, 750);
    return {
      ...common,
      wattage: watt,
      formFactor: /SFX/i.test(name) ? "SFX" : "ATX",
      modular: !/A650BN|System Power/i.test(name),
      efficiency: /Titanium|TX/i.test(name) ? "80+ Titanium" : /Platinum|PX|P6/i.test(name) ? "80+ Platinum" : "80+ Gold",
      pcie5Ready: /PCIE5|ATX 3|GF3|Vertex|Shift/i.test(name),
    };
  }

  if (category === "hdds") {
    const capacity = parseCapacityGb(name, 4000);
    return {
      ...common,
      capacityGB: capacity,
      formFactor: "3.5 inch",
      interface: "SATA III",
      rpm: capacity >= 10000 ? 7200 : 5400,
      cacheMB: capacity >= 8000 ? 256 : 128,
    };
  }

  if (category === "ssds") {
    const capacity = parseCapacityGb(name, 1000);
    const sata = /870\s*EVO|870\s*QVO|MX500|BX500|SA510|A400|KC600|CS900/i.test(name);
    return {
      ...common,
      capacityGB: capacity,
      interface: sata ? "SATA III" : "PCIe NVMe",
      formFactor: sata ? "2.5 inch" : "M.2 2280",
      readMBs: sata ? 560 : 3500 + (hashText(name) % 4500),
      writeMBs: sata ? 530 : 3000 + (hashText(name) % 3800),
    };
  }

  if (category === "opticalDrives") {
    return {
      ...common,
      driveType: /Blu-ray|BDR|WH16/i.test(name) ? "Blu-ray/DVD/CD" : "DVD/CD",
      interface: /External|USB|ZenDrive|Slim/i.test(name) ? "USB" : "SATA",
      writingSupport: ["CD-R/RW", "DVD-R/RW", /Blu-ray|BDR|WH16/i.test(name) ? "BD-R/RE" : "-"],
      external: /External|USB|ZenDrive|Slim/i.test(name),
    };
  }

  if (category === "cpuAirCoolers") {
    return {
      ...common,
      coolerType: "Air",
      heatpipes: clamp(4 + (hashText(name) % 4), 4, 8),
      fans: /D15|Pro|Assassin|AK620|HX/i.test(name) ? 2 : 1,
      heightMm: clamp(135 + (hashText(name) % 35), 135, 170),
      socketSupport: ["AM4", "AM5", "LGA1700"],
    };
  }

  if (category === "cpuLiquidCoolers") {
    const radiator = parseNumber(name, /(240|280|360|420)/, 240);
    return {
      ...common,
      coolerType: "Liquid AIO",
      radiatorMm: radiator,
      pumpSpeedRpm: 2500 + (hashText(name) % 800),
      rgb: /RGB|Elite|LCD|Ryujin|HydroShift/i.test(name),
      socketSupport: ["AM4", "AM5", "LGA1700"],
    };
  }

  if (category === "monitors") {
    const size = parseNumber(name, /(\d{2})/, 27);
    return {
      ...common,
      sizeInch: size,
      resolution: /4K|U32|U27|M32U|PA329|U3223|VX3276/i.test(name) ? "3840x2160" : /34|G9|AW3423/i.test(name) ? "3440x1440" : "2560x1440",
      panel: /OLED|QD-OLED/i.test(name) ? "OLED" : /IPS|UQ|M27|M32|ProArt|Nitro|ViewSonic/i.test(name) ? "IPS" : "VA",
      refreshHz: /360|QD-OLED|AW2723|XG270/i.test(name) ? 360 : /240|270|280|QM|GX/i.test(name) ? 240 : /165|170|180|GP850|R27/i.test(name) ? 165 : 144,
      adaptiveSync: true,
    };
  }

  if (category === "keyboards") {
    return {
      ...common,
      layout: /TKL|75|65|60|96|84|87/i.test(name) ? "Compact" : "Full-size",
      connection: /Wireless|HyperSpeed|AIR|Pro|Keys\s*S|Mechanical/i.test(name) ? ["USB-C", "Bluetooth", "2.4GHz"] : ["USB"],
      switchType: /Huntsman/i.test(name) ? "Optical" : /Mechanical|K70|G915|Azoth|Apex|Keychron|Ducky|Akko|Alloy|BlackWidow/i.test(name) ? "Mechanical" : "Membrane",
      backlight: true,
      hotSwappable: /Keychron|Glorious|Akko|Azoth|Q1|Q6|Q60/i.test(name),
    };
  }

  if (category === "mice") {
    return {
      ...common,
      connection: /Wireless|Lightspeed|Superlight|Pro|2\b|we/i.test(name) ? ["2.4GHz", "Bluetooth/USB"] : ["USB"],
      sensorDpi: clamp(12000 + (hashText(name) % 22001), 12000, 34000),
      weightG: clamp(52 + (hashText(name) % 70), 52, 122),
      buttons: clamp(5 + (hashText(name) % 8), 5, 12),
      category: /MMO|Scimitar|Naga/i.test(name) ? "MMO" : /Master|Office/i.test(name) ? "Productivity" : "Gaming",
    };
  }

  if (category === "operatingSystems") {
    return {
      ...common,
      family: /Windows/i.test(name)
        ? "Windows"
        : /Ubuntu|Debian|Fedora|openSUSE|Arch|Manjaro|Pop|Zorin|elementary|Oracle|Rocky|Alma|Red Hat|NixOS|KDE/i.test(name)
          ? "Linux"
          : /FreeBSD/i.test(name)
            ? "BSD"
            : /Proxmox|TrueNAS/i.test(name)
              ? "Platform"
              : "Other",
      architecture: "x86_64",
      licenseType: /Windows|Red Hat/i.test(name) ? "Paid" : "Open-source / Free",
      edition: /Server|Enterprise|Pro|Education|Home|LTSC|Workstations|IoT/i.test(name) ? "Commercial" : "Community",
      supportsUefi: true,
    };
  }

  return common;
}

